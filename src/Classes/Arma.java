package Classes;

public class Arma {
	
	private String nome;
	private int dano;
	
	public Arma(String nome, int dano){
		this.nome=nome;
		this.dano=dano;
	}
	
	public String getNome(){
		return this.nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public int getDano() {
		return dano;
	}

	public void setDano(int dano){
		this.dano=dano;
	}
}
